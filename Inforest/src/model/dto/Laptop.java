package model.dto;

public class Laptop extends Product{

	private String lPurpose;
	private String lDisplay;
	private String lCPU;
	private String lRAMMemory;
	private String lOS;
	private String productId;
	private String lSSD;
	public  String getlPurpose() {
		return lPurpose;
	}
	public void setlPurpose(String lPurpose) {
		this.lPurpose = lPurpose;
	}
	public String getlDisplay() {
		return lDisplay;
	}
	public void setlDisplay(String lDisplay) {
		this.lDisplay = lDisplay;
	}
	public String getlCPU() {
		return lCPU;
	}
	public void setlCPU(String lCPU) {
		this.lCPU = lCPU;
	}
	public String getlRAMMemory() {
		return lRAMMemory;
	}
	public void setlRAMMemory(String lRAMMemory) {
		this.lRAMMemory = lRAMMemory;
	}
	public String getlOS() {
		return lOS;
	}
	public void setlOS(String lOS) {
		this.lOS = lOS;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getlSSD() {
		return lSSD;
	}
	public void setlSSD(String lSSD) {
		this.lSSD = lSSD;
	}

	
	
}
